class Aeroplane
{
	String company;
	int noOfSeats;
	double ticketPrice;
	String source;
	String destination;
	
	Aeroplane()
	{
		System.out.println("created aeroplane with no parameter");
		System.out.println("Company:"+company);
		System.out.println("NoOfSeats:"+noOfSeats);
		System.out.println("TicketPrice:"+ticketPrice);
		System.out.println("Source:"+source);
		System.out.println("Destination:"+destination);
		
		
		this.company=company;
		this.noOfSeats=noOfSeats;
		this.ticketPrice=ticketPrice;
		this.source=source;
		this.destination=destination;
		
		System.out.println("Company updated:"+company);
		System.out.println("NoOfSeats updated:"+noOfSeats);
		System.out.println("TicketPrice updated:"+ticketPrice);
		System.out.println("Source updated:"+source);
		System.out.println("Destination updated:"+destination);
		
		
	}
	
	Aeroplane(String company,String source,String destination)
	{
		System.out.println("created aeroplane using String,String,String");
		System.out.println("Company:"+company);
		System.out.println("NoOfSeats:"+noOfSeats);
		System.out.println("TicketPrice:"+ticketPrice);
		System.out.println("Source:"+source);
		System.out.println("Destination:"+destination);
		
		
		this.company=company;
		this.noOfSeats=noOfSeats;
		this.ticketPrice=ticketPrice;
		this.source=source;
		this.destination=destination;
		
		System.out.println("Company updated:"+company);
		System.out.println("NoOfSeats updated:"+noOfSeats);
		System.out.println("TicketPrice updated:"+ticketPrice);
		System.out.println("Source updated:"+source);
		System.out.println("Destination updated:"+destination);
		
		
	}
	
	Aeroplane(double ticketPrice,String source,String destination)
	{
		System.out.println("created aeroplane using double,String,String");
		System.out.println("Company:"+company);
		System.out.println("NoOfSeats:"+noOfSeats);
		System.out.println("TicketPrice:"+ticketPrice);
		System.out.println("Source:"+source);
		System.out.println("Destination:"+destination);
		
		
		this.company=company;
		this.noOfSeats=noOfSeats;
		this.ticketPrice=ticketPrice;
		this.source=source;
		this.destination=destination;
		
		System.out.println("Company updated:"+company);
		System.out.println("NoOfSeats updated:"+noOfSeats);
		System.out.println("TicketPrice updated:"+ticketPrice);
		System.out.println("Source updated:"+source);
		System.out.println("Destination updated:"+destination);
		
		
	}
	
	Aeroplane(String company,int noOfSeats,double ticketPrice,String source,String destination)
	{
		System.out.println("created aeroplane using String,int,double,String,String");
		System.out.println("Company:"+company);
		System.out.println("NoOfSeats:"+noOfSeats);
		System.out.println("TicketPrice:"+ticketPrice);
		System.out.println("Source:"+source);
		System.out.println("Destination:"+destination);
		
		
		this.company=company;
		this.noOfSeats=noOfSeats;
		this.ticketPrice=ticketPrice;
		this.source=source;
		this.destination=destination;
		
		System.out.println("Company updated:"+company);
		System.out.println("NoOfSeats updated:"+noOfSeats);
		System.out.println("TicketPrice updated:"+ticketPrice);
		System.out.println("Source updated:"+source);
		System.out.println("Destination updated:"+destination);
		
		
	}
}